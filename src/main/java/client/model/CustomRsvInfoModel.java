package client.model;

public class CustomRsvInfoModel {

	private String store_seq;
	private String client_seq;
	private String custom_rsv_ymd;
	private String custom_rsv_year;
	private String custom_rsv_month;
	private String custom_rsv_date;
	private String custom_rsv_switch;
	
	public String getStore_seq() {
		return store_seq;
	}
	public void setStore_seq(String store_seq) {
		this.store_seq = store_seq;
	}
	public String getClient_seq() {
		return client_seq;
	}
	public void setClient_seq(String client_seq) {
		this.client_seq = client_seq;
	}
	public String getCustom_rsv_ymd() {
		return custom_rsv_ymd;
	}
	public void setCustom_rsv_ymd(String custom_rsv_ymd) {
		this.custom_rsv_ymd = custom_rsv_ymd;
	}
	public String getCustom_rsv_year() {
		return custom_rsv_year;
	}
	public void setCustom_rsv_year(String custom_rsv_year) {
		this.custom_rsv_year = custom_rsv_year;
	}
	public String getCustom_rsv_month() {
		return custom_rsv_month;
	}
	public void setCustom_rsv_month(String custom_rsv_month) {
		this.custom_rsv_month = custom_rsv_month;
	}
	public String getCustom_rsv_date() {
		return custom_rsv_date;
	}
	public void setCustom_rsv_date(String custom_rsv_date) {
		this.custom_rsv_date = custom_rsv_date;
	}
	public String getCustom_rsv_switch() {
		return custom_rsv_switch;
	}
	public void setCustom_rsv_switch(String custom_rsv_switch) {
		this.custom_rsv_switch = custom_rsv_switch;
	}
	
	
}
