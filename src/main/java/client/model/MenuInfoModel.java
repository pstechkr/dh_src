package client.model;

public class MenuInfoModel {

	private String menu_seq;
	private String store_seq;
	private String menu_name;
	private String menu_instruction;
	private String menu_price;
	private String menu_time;
	private String menu_day;

	
	public String getMenu_seq() {
		return menu_seq;
	}
	public void setMenu_seq(String menu_seq) {
		this.menu_seq = menu_seq;
	}
	public String getStore_seq() {
		return store_seq;
	}
	public void setStore_seq(String store_seq) {
		this.store_seq = store_seq;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public String getMenu_instruction() {
		return menu_instruction;
	}
	public void setMenu_instruction(String menu_instruction) {
		this.menu_instruction = menu_instruction;
	}
	public String getMenu_price() {
		return menu_price;
	}
	public void setMenu_price(String menu_price) {
		this.menu_price = menu_price;
	}
	public String getMenu_time() {
		return menu_time;
	}
	public void setMenu_time(String menu_time) {
		this.menu_time = menu_time;
	}
	public String getMenu_day() {
		return menu_day;
	}
	public void setMenu_day(String menu_day) {
		this.menu_day = menu_day;
	}

}
